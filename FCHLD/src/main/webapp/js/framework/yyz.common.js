/**
 * 메시지 @ 치환하여 값을 리턴 - 다중값으로 수정 예정
 * 작성자 : 조준희 - 2013.11.14
 * (인자)
 * msgNo : 메시지 번호
 * msg : @ 메시지 값
 */
function fnMessageReplace(msgNo, msgVal){
    msgNo = msgNo.replace(/@/, msgVal);
    return msgNo;
}